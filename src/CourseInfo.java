

public class CourseInfo {
	private String title;
	private int sum_students;
	private double avg_gpa;
	private int number;
	private String subject;
	private String name;
	

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public int getSum_students() {
		return sum_students;
	}

	public void setSum_students(int sum_students) {
		this.sum_students = sum_students;
	}

	public double getAvg_gpa() {
		return avg_gpa;
	}

	public void setAvg_gpa(double avg_gpa) {
		this.avg_gpa = avg_gpa;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}
}
